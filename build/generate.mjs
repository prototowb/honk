#!/usr/bin/env node
// SPMC build generator — emits per-surface artifacts from the single origin.
//
// Origin: spmc-server/lib/tools.js (tool schemas) + spmc-server/lib/specs.js
// (platform limits). Nothing here is hand-authored capability data; it is all
// derived. See BUILD_CONCEPT.md.
//
//   node build/generate.mjs           regenerate all artifacts in place
//   node build/generate.mjs --check   fail (exit 1) if any artifact is stale
//
// This is the Phase-1 skeleton: it currently emits the generated tool catalog
// (TOOLS.md). Per-surface targets (plugin.json, skills, MCP configs, README /
// Hermes injection) hang off the same origin + writeArtifact()/check plumbing.

import { readFileSync, writeFileSync, existsSync } from 'fs';
import { dirname, join, relative }                 from 'path';
import { fileURLToPath }                           from 'url';

import { TOOLS }          from '../spmc-server/lib/tools.js';
import { PLATFORM_SPECS } from '../spmc-server/lib/specs.js';

const ROOT = join(dirname(fileURLToPath(import.meta.url)), '..');
const pkg  = JSON.parse(readFileSync(join(ROOT, 'spmc-server', 'package.json'), 'utf8'));

const GENERATED_HEADER = (cmd) =>
  `<!-- AUTO-GENERATED by \`npm run build\` (build/generate.mjs) — DO NOT EDIT.\n` +
  `     Edit the origin (spmc-server/lib/tools.js + lib/specs.js) and regenerate.\n` +
  `     Regenerate: npm run build   ·   Verify: ${cmd} -->\n`;

// ─── Origin helpers ─────────────────────────────────────────────────────────

// Map a tool to its category by name — the grouping that lives only in comments
// in tools.js. Inferred here so it stays single-sourced (no parallel list).
const PUBLISH_PLATFORMS = ['x', 'instagram', 'tiktok', 'facebook', 'threads', 'bluesky'];

function categoryOf(name) {
  if (PUBLISH_PLATFORMS.some(p => name.startsWith(p + '_'))) return 'Publishing & status';
  if (name.startsWith('queue_'))   return 'Queue';
  if (name.startsWith('media_'))   return 'Media';
  if (name === 'rate_limits' || name.startsWith('analytics_')) return 'Observability';
  return 'Content intelligence'; // content_*, config_doctor, audit_log, schedule_check
}

// The platform a publishing tool targets, for joining in its limit from specs.
function platformOf(name) {
  return PUBLISH_PLATFORMS.find(p => name.startsWith(p + '_')) ?? null;
}

function limitFor(platform) {
  const t = PLATFORM_SPECS[platform]?.text;
  return t ? `${t.max} ${t.unit}` : '';
}

function inputs(tool) {
  const props = Object.keys(tool.inputSchema?.properties ?? {});
  const req   = new Set(tool.inputSchema?.required ?? []);
  const fmt   = (k) => `\`${k}\`${tool.inputSchema.properties[k].type ? ` (${tool.inputSchema.properties[k].type})` : ''}`;
  return {
    required: props.filter(k => req.has(k)).map(fmt).join(', ') || '—',
    optional: props.filter(k => !req.has(k)).map(fmt).join(', ') || '—',
  };
}

// ─── Artifact: TOOLS.md (generated tool catalog) ─────────────────────────────

function renderToolCatalog() {
  const byCat = new Map();
  for (const t of TOOLS) {
    const c = categoryOf(t.name);
    if (!byCat.has(c)) byCat.set(c, []);
    byCat.get(c).push(t);
  }

  const order = ['Publishing & status', 'Content intelligence', 'Observability', 'Queue', 'Media'];
  const cats  = [...order.filter(c => byCat.has(c)), ...[...byCat.keys()].filter(c => !order.includes(c))];

  let md = GENERATED_HEADER('npm run build:check');
  md += `\n# SPMC Tool Reference\n\n`;
  md += `**${TOOLS.length} tools** · generated from \`spmc-server/lib/tools.js\` + \`lib/specs.js\` · server v${pkg.version}\n`;

  for (const cat of cats) {
    md += `\n## ${cat}\n\n`;
    md += `| Tool | Required | Optional | Platform limit | Description |\n`;
    md += `|------|----------|----------|----------------|-------------|\n`;
    for (const t of byCat.get(cat)) {
      const { required, optional } = inputs(t);
      const plat      = platformOf(t.name);
      const textField = plat ? PLATFORM_SPECS[plat]?.text?.field : null;
      // Only show a limit for tools that actually carry the platform's text
      // payload (e.g. not tiktok_check_publish_status).
      const limit = textField && t.inputSchema?.properties?.[textField] ? limitFor(plat) : '';
      const desc  = t.description.replace(/\|/g, '\\|').replace(/\n/g, ' ');
      md += `| \`${t.name}\` | ${required} | ${optional} | ${limit || '—'} | ${desc} |\n`;
    }
  }
  return md;
}

// ─── Artifact registry + write/check plumbing ────────────────────────────────

const ARTIFACTS = [
  { path: 'TOOLS.md', render: renderToolCatalog },
];

function run({ check }) {
  let drift = 0;
  for (const a of ARTIFACTS) {
    const abs      = join(ROOT, a.path);
    const next     = a.render();
    const current  = existsSync(abs) ? readFileSync(abs, 'utf8') : null;
    const rel       = relative(ROOT, abs).replace(/\\/g, '/');

    if (check) {
      if (current !== next) {
        drift++;
        console.error(`✗ stale: ${rel}${current === null ? ' (missing)' : ''}`);
      } else {
        console.log(`✓ up to date: ${rel}`);
      }
    } else {
      if (current === next) {
        console.log(`= unchanged: ${rel}`);
      } else {
        writeFileSync(abs, next);
        console.log(`✎ wrote: ${rel}`);
      }
    }
  }

  if (check && drift > 0) {
    console.error(`\n${drift} artifact(s) out of date. Run \`npm run build\` and commit.`);
    process.exit(1);
  }
  if (check) console.log('\nAll generated artifacts are up to date.');
}

run({ check: process.argv.includes('--check') });
