#!/usr/bin/env node
// SPMC build generator — emits per-surface artifacts from the single origin.
//
// Origin (two halves):
//   • Machine facts — spmc-server/lib/tools.js (tool schemas) + lib/specs.js
//     (platform limits) + lib/config.js (media providers) + package.json.
//   • Prose — capabilities/ (hand-authored skill/Hermes copy) carrying tokens
//     ({{limit:…}}, {{unit:…}}, {{tool:…}}) resolved against the machine facts,
//     so limits and tool names in prose can't drift from what the server serves.
// See BUILD_CONCEPT.md.
//
//   node build/generate.mjs           regenerate all artifacts in place
//   node build/generate.mjs --check   fail (exit 1) if any artifact is stale
//
// Emits: TOOLS.md, .claude-plugin/plugin.json, the gen:tools regions in
// README.md + hermes/CONTEXT.md, the three MCP configs, every skills/*/SKILL.md
// (from capabilities/skills/), and hermes/SKILLS.md (from capabilities/hermes/).

import { readFileSync, writeFileSync, existsSync, readdirSync, mkdirSync } from 'fs';
import { dirname, join, relative }                 from 'path';
import { fileURLToPath }                           from 'url';

import { TOOLS }           from '../spmc-server/lib/tools.js';
import { PLATFORM_SPECS }  from '../spmc-server/lib/specs.js';
import { MEDIA_PROVIDERS } from '../spmc-server/lib/config.js';

const ROOT = join(dirname(fileURLToPath(import.meta.url)), '..');
const pkg  = JSON.parse(readFileSync(join(ROOT, 'spmc-server', 'package.json'), 'utf8'));

const GENERATED_HEADER = (cmd) =>
  `<!-- AUTO-GENERATED by \`npm run build\` (build/generate.mjs) — DO NOT EDIT.\n` +
  `     Edit the origin (spmc-server/lib/tools.js + lib/specs.js) and regenerate.\n` +
  `     Regenerate: npm run build   ·   Verify: ${cmd} -->\n`;

// ─── Origin helpers ─────────────────────────────────────────────────────────

// Map a tool to its category by name — the grouping that lives only in comments
// in tools.js. Inferred here so it stays single-sourced (no parallel list).
const PUBLISH_PLATFORMS = ['x', 'instagram', 'tiktok', 'facebook', 'threads', 'bluesky'];

function categoryOf(name) {
  if (PUBLISH_PLATFORMS.some(p => name.startsWith(p + '_'))) return 'Publishing & status';
  if (name.startsWith('queue_'))   return 'Queue';
  if (name.startsWith('media_'))   return 'Media';
  if (name === 'rate_limits' || name.startsWith('analytics_')) return 'Observability';
  return 'Content intelligence'; // content_*, config_doctor, audit_log, schedule_check
}

// The platform a publishing tool targets, for joining in its limit from specs.
function platformOf(name) {
  return PUBLISH_PLATFORMS.find(p => name.startsWith(p + '_')) ?? null;
}

function limitFor(platform) {
  const t = PLATFORM_SPECS[platform]?.text;
  return t ? `${t.max} ${t.unit}` : '';
}

function inputs(tool) {
  const props = Object.keys(tool.inputSchema?.properties ?? {});
  const req   = new Set(tool.inputSchema?.required ?? []);
  const fmt   = (k) => `\`${k}\`${tool.inputSchema.properties[k].type ? ` (${tool.inputSchema.properties[k].type})` : ''}`;
  return {
    required: props.filter(k => req.has(k)).map(fmt).join(', ') || '—',
    optional: props.filter(k => !req.has(k)).map(fmt).join(', ') || '—',
  };
}

// ─── Artifact: TOOLS.md (generated tool catalog) ─────────────────────────────

function toolsByCategory() {
  const byCat = new Map();
  for (const t of TOOLS) {
    const c = categoryOf(t.name);
    if (!byCat.has(c)) byCat.set(c, []);
    byCat.get(c).push(t);
  }
  const order = ['Publishing & status', 'Content intelligence', 'Observability', 'Queue', 'Media'];
  const cats  = [...order.filter(c => byCat.has(c)), ...[...byCat.keys()].filter(c => !order.includes(c))];
  return { byCat, cats };
}

// The categorized tool tables — shared verbatim by TOOLS.md and the injected
// README / Hermes regions. `heading` is the markdown prefix per category: `##`
// under the TOOLS.md H1, `###` under a host doc's existing H2 section.
function renderToolTables(heading = '##') {
  const { byCat, cats } = toolsByCategory();
  let md = '';
  for (const cat of cats) {
    md += `\n${heading} ${cat}\n\n`;
    md += `| Tool | Required | Optional | Platform limit | Description |\n`;
    md += `|------|----------|----------|----------------|-------------|\n`;
    for (const t of byCat.get(cat)) {
      const { required, optional } = inputs(t);
      const plat      = platformOf(t.name);
      const textField = plat ? PLATFORM_SPECS[plat]?.text?.field : null;
      // Only show a limit for tools that actually carry the platform's text
      // payload (e.g. not tiktok_check_publish_status).
      const limit = textField && t.inputSchema?.properties?.[textField] ? limitFor(plat) : '';
      const desc  = t.description.replace(/\|/g, '\\|').replace(/\n/g, ' ');
      md += `| \`${t.name}\` | ${required} | ${optional} | ${limit || '—'} | ${desc} |\n`;
    }
  }
  return md;
}

function renderToolCatalog() {
  let md = GENERATED_HEADER('npm run build:check');
  md += `\n# SPMC Tool Reference\n\n`;
  md += `**${TOOLS.length} tools** · generated from \`spmc-server/lib/tools.js\` + \`lib/specs.js\` · server v${pkg.version}\n`;
  md += renderToolTables('##');
  return md;
}

// ─── Artifact: .claude-plugin/plugin.json (Claude plugin manifest) ───────────

function renderPluginManifest() {
  const manifest = {
    name:        pkg.name,
    version:     pkg.version,
    description: pkg.description,
    author:      { name: typeof pkg.author === 'string' ? pkg.author : pkg.author?.name },
    license:     pkg.license,
    keywords:    pkg.keywords,
  };
  return JSON.stringify(manifest, null, 2) + '\n';
}

// ─── Artifact: MCP server configs (.mcp.json + hermes + Claude Desktop) ───────
// One server, three surfaces. All render from the same shape so they can't drift
// (the "same server, three shapes, one a live defect" problem). The differences
// are intentional: launch path (plugin var vs absolute vs npx) and whether an
// env block is present (Claude Code injects creds via ${VAR}; the others self-
// load from ~/.claude/spmc.env at startup, so they carry no env).

// The 17 credential env keys, single-sourced: every platform's credentials plus
// every media provider's keys. Adding a platform/provider updates all configs.
function credentialEnvKeys() {
  return [
    ...Object.values(PLATFORM_SPECS).flatMap(s => s.credentials),
    ...Object.values(MEDIA_PROVIDERS).flat(),
  ];
}

function envPlaceholders() {
  const env = {};
  for (const k of credentialEnvKeys()) env[k] = '${' + k + '}';
  return env;
}

// Shared builder: `command` + `args`, optionally an `env` block, optionally a
// leading `_comment` (only Claude Desktop's config tolerates unknown keys, and
// it carries manual-setup guidance). JSON files can't hold a DO-NOT-EDIT comment
// without risking a schema validator, so drift is caught by build:check instead.
function mcpConfig({ command, args, env, comment }) {
  const spmc = { command, args };
  if (env) spmc.env = env;
  const out = {};
  if (comment) out._comment = comment;
  out.mcpServers = { spmc };
  return JSON.stringify(out, null, 2) + '\n';
}

// Claude Code plugin: resolves the server path via ${CLAUDE_PLUGIN_ROOT} and
// injects credentials from the running environment as ${VAR} placeholders.
function renderMcpJson() {
  return mcpConfig({
    command: 'node',
    args: ['${CLAUDE_PLUGIN_ROOT}/spmc-server/run.js'],
    env: envPlaceholders(),
  });
}

// Hermes: outside the Claude plugin ecosystem, so no ${CLAUDE_PLUGIN_ROOT} — it
// needs an absolute path to run.js (machine-specific by nature). No env block:
// run.js self-loads creds from ~/.claude/spmc.env.
function renderHermesMcpConfig() {
  return mcpConfig({
    command: 'node',
    args: [join(ROOT, 'spmc-server', 'run.js')],
  });
}

// Claude Desktop: the no-marketplace manual fallback. Uses npx so no clone path;
// creds self-load from ~/.claude/spmc.env. Keeps the setup guidance in _comment.
function renderClaudeDesktopConfig() {
  return mcpConfig({
    comment: [
      'AUTO-GENERATED by `npm run build` (build/generate.mjs) — do not edit; rebuild instead.',
      'Drop the mcpServers block into your Claude Desktop config.',
      'Windows: %APPDATA%\\Claude\\claude_desktop_config.json',
      'macOS:   ~/Library/Application Support/Claude/claude_desktop_config.json',
      '',
      "OPTION A — npm (after 'npm install -g spmc', or 'npm pack && npm install -g spmc-" + pkg.version + ".tgz'):",
      '  Use the npx block below. Credentials load from ~/.claude/spmc.env automatically.',
      '',
      'OPTION B — local clone (replace the path with your actual clone location):',
      '  { "command": "node", "args": ["/path/to/spmc-server/run.js"] }',
      '',
      'Either way, store credentials in ~/.claude/spmc.env (see .env.example).',
    ],
    command: 'npx',
    args: ['-y', 'spmc'],
  });
}

// Compare line-ending-independent: the generator emits LF, but git autocrlf
// can leave CRLF in the working tree after a clone/checkout. Without this,
// --check would report spurious drift on a fresh checkout (esp. on Windows).
// Also normalizes host files before marker injection so the whole-file compare
// (and the written output) is consistently LF.
const norm = (s) => (s == null ? s : s.replace(/\r\n/g, '\n'));

// ─── Artifact: injected tool tables (README.md + hermes/CONTEXT.md) ───────────

const TOOLS_MARKER = 'gen:tools';

// The block placed between <!-- gen:tools:start --> / <!-- gen:tools:end --> in
// host docs: a provenance line + the same categorized tables as TOOLS.md, one
// heading level deeper so they sit under the host's existing H2 section.
function renderInjectedTools() {
  const note = `_${TOOLS.length} tools — generated from \`lib/tools.js\` + \`lib/specs.js\`. ` +
               `Do not edit between these markers; run \`npm run build\`._\n`;
  return note + renderToolTables('###');
}

// Read a host file, replace the content between a marker pair with bodyFn(),
// and return the WHOLE file (LF-normalized). Returning the whole file lets the
// existing whole-file compare in run() cover the injected region for free.
function injectArtifact(hostRel, marker, bodyFn) {
  return () => {
    const start = `<!-- ${marker}:start -->`;
    const end   = `<!-- ${marker}:end -->`;
    const src   = norm(readFileSync(join(ROOT, hostRel), 'utf8'));
    const si = src.indexOf(start);
    const ei = src.indexOf(end);
    if (si === -1 || ei === -1 || ei < si) {
      throw new Error(`inject: markers '${marker}' missing or out of order in ${hostRel}`);
    }
    return `${src.slice(0, si + start.length)}\n${bodyFn()}\n${src.slice(ei)}`;
  };
}

// ─── Token resolution (capabilities prose → origin facts) ────────────────────
// Tokens in capabilities/*.md bind hand-authored prose to the single origin so
// limit values and tool names can't drift from what the server serves:
//   {{limit:bluesky.text.max}} → PLATFORM_SPECS.bluesky.text.max   (300)
//   {{unit:bluesky.text}}      → PLATFORM_SPECS.bluesky.text.unit  ("graphemes")
//   {{tool:bluesky_post}}      → the tool name, validated against TOOLS
// Resolution is a 1:1 specs object-path lookup, no aliases. Any unresolvable
// token — bad path, unknown tool, or an unknown token kind — fails the build; a
// bad token is a build error, never silently shipped.

function specAt(path) {
  return path.split('.').reduce((o, k) => (o == null ? undefined : o[k]), PLATFORM_SPECS);
}

function resolveTokens(text, where) {
  const out = text.replace(/\{\{(limit|unit|tool):([^}]+)\}\}/g, (_m, kind, arg) => {
    arg = arg.trim();
    if (kind === 'limit') {
      const v = specAt(arg);
      if (typeof v !== 'number' && typeof v !== 'string') {
        throw new Error(`${where}: {{limit:${arg}}} does not resolve to a value (PLATFORM_SPECS.${arg})`);
      }
      return String(v);
    }
    if (kind === 'unit') {
      const o = specAt(arg);
      if (o == null || typeof o.unit !== 'string') {
        throw new Error(`${where}: {{unit:${arg}}} has no string .unit (PLATFORM_SPECS.${arg}.unit)`);
      }
      return o.unit;
    }
    // kind === 'tool'
    if (!TOOLS.some(t => t.name === arg)) {
      throw new Error(`${where}: {{tool:${arg}}} is not a known tool in lib/tools.js`);
    }
    return arg;
  });
  const stray = out.match(/\{\{[^}]*\}?\}/);
  if (stray) {
    throw new Error(`${where}: unresolved token "${stray[0]}" (expected {{limit:…}}, {{unit:…}}, or {{tool:…}})`);
  }
  return out;
}

// ─── Artifact: skills/*/SKILL.md (from capabilities/skills/) ──────────────────

// Skill-name rules from the Agent Skills spec: ≤64 chars, lowercase + digits +
// hyphens only, and must not contain the reserved words "claude"/"anthropic".
// Enforced at generate time so a bad name fails the build, not the skill loader.
function validateSkillName(name, where) {
  if (!/^[a-z0-9-]{1,64}$/.test(name)) {
    throw new Error(`${where}: invalid skill name "${name}" (lowercase/digits/hyphens, ≤64 chars)`);
  }
  if (/claude|anthropic/.test(name)) {
    throw new Error(`${where}: skill name "${name}" contains a reserved word (claude/anthropic)`);
  }
}

// DO-NOT-EDIT header for prose-sourced artifacts. For SKILL.md it is inserted
// AFTER the closing frontmatter fence (never before): skill loaders require the
// `---` to be the first bytes, so a leading header would break them.
const SOURCE_HEADER = (srcRel) =>
  `<!-- AUTO-GENERATED by \`npm run build\` (build/generate.mjs) — DO NOT EDIT.\n` +
  `     Edit the prose source: ${srcRel}   ·   Regenerate: npm run build   ·   Verify: npm run build:check -->\n`;

// Emit skills/<name>/SKILL.md from capabilities/skills/<name>.md: validate the
// frontmatter name, resolve tokens against the origin, and splice the header in
// just after the frontmatter. Returns the whole file so run()'s whole-file
// compare guards every byte for free.
function renderSkill(name) {
  const srcRel = `capabilities/skills/${name}.md`;
  const raw    = norm(readFileSync(join(ROOT, srcRel), 'utf8'));
  if (!raw.startsWith('---\n')) throw new Error(`${srcRel}: must open with YAML frontmatter (---)`);

  const fenceEnd = raw.indexOf('\n---\n', 4);
  if (fenceEnd === -1) throw new Error(`${srcRel}: unterminated frontmatter`);
  const frontmatter = raw.slice(0, fenceEnd + '\n---\n'.length);
  const body        = raw.slice(frontmatter.length);

  const nameMatch = frontmatter.match(/^name:\s*(.+?)\s*$/m);
  if (!nameMatch) throw new Error(`${srcRel}: frontmatter has no name:`);
  validateSkillName(nameMatch[1], srcRel);
  if (nameMatch[1] !== name) {
    throw new Error(`${srcRel}: frontmatter name "${nameMatch[1]}" must match file name "${name}"`);
  }

  return resolveTokens(frontmatter + SOURCE_HEADER(srcRel) + body, srcRel);
}

// hermes/SKILLS.md — whole-file prose (no frontmatter), so the header goes at
// the very top. Same token resolution as skills.
function renderHermesSkills() {
  const srcRel = 'capabilities/hermes/SKILLS.md';
  const raw    = norm(readFileSync(join(ROOT, srcRel), 'utf8'));
  return resolveTokens(SOURCE_HEADER(srcRel) + '\n' + raw, srcRel);
}

// ─── Artifact registry + write/check plumbing ────────────────────────────────

// Skills are discovered from the source tree, so adding capabilities/skills/<x>.md
// is enough to make skills/<x>/SKILL.md a tracked, build-checked artifact.
const SKILL_NAMES = readdirSync(join(ROOT, 'capabilities', 'skills'))
  .filter(f => f.endsWith('.md'))
  .map(f => f.slice(0, -'.md'.length))
  .sort();

const SKILL_ARTIFACTS = SKILL_NAMES.map(name => ({
  path:   `skills/${name}/SKILL.md`,
  render: () => renderSkill(name),
}));

const ARTIFACTS = [
  { path: 'TOOLS.md',                    render: renderToolCatalog },
  { path: '.claude-plugin/plugin.json',  render: renderPluginManifest },
  { path: 'README.md',                   render: injectArtifact('README.md',        TOOLS_MARKER, renderInjectedTools) },
  { path: 'hermes/CONTEXT.md',           render: injectArtifact('hermes/CONTEXT.md', TOOLS_MARKER, renderInjectedTools) },
  { path: '.mcp.json',                   render: renderMcpJson },
  { path: 'hermes/mcp-config.json',      render: renderHermesMcpConfig },
  { path: 'claude_desktop_config.json',  render: renderClaudeDesktopConfig },
  ...SKILL_ARTIFACTS,
  { path: 'hermes/SKILLS.md',            render: renderHermesSkills },
];

function run({ check }) {
  let drift = 0;
  for (const a of ARTIFACTS) {
    const abs      = join(ROOT, a.path);
    const next     = a.render();
    const current  = existsSync(abs) ? readFileSync(abs, 'utf8') : null;
    const rel       = relative(ROOT, abs).replace(/\\/g, '/');
    const matches   = norm(current) === next;

    if (check) {
      if (!matches) {
        drift++;
        console.error(`✗ stale: ${rel}${current === null ? ' (missing)' : ''}`);
      } else {
        console.log(`✓ up to date: ${rel}`);
      }
    } else {
      if (matches) {
        console.log(`= unchanged: ${rel}`);
      } else {
        mkdirSync(dirname(abs), { recursive: true });
        writeFileSync(abs, next);
        console.log(`✎ wrote: ${rel}`);
      }
    }
  }

  if (check && drift > 0) {
    console.error(`\n${drift} artifact(s) out of date. Run \`npm run build\` and commit.`);
    process.exit(1);
  }
  if (check) console.log('\nAll generated artifacts are up to date.');
}

run({ check: process.argv.includes('--check') });
